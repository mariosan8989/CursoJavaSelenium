package ciclos;

public class EjemploWhile {

	public void EjemWhile()
	{
		int a=1;
		
		while(a <=4)
		{
			System.out.println("Valor de A:"+a);
			a++;
		}
	}
	
	public void EjemploDoWhile()
	{ int b = 10;
	
	do {
		System.out.println("el valor de B es:"+ b);
		b++;
	}while(b<20);
		
	}
}

package metodos;

public class EjemploMetodos {

	public static int suma(int a, int b) 
	{
		int c = a+b;
		return c;
	}
	
	public static int suma(int a, int b,int c) 
	{
		int d  = a+b+c;
		return d;
	}
	
	public static String carro(int a)
	{
		String[] cars = {"volvo","Kia Rio","Ford"};
		return cars[a];
	}
}
